package basic;

public class EscapeSequence {
	public static void main(String[] args) {
		System.out.println("A\tB\tC\t");
		System.out.println("300\t200\t100\t");
		System.out.println("hello\n\njava");
		System.out.println("reverse slash : \\");
		System.out.println("해리는 말했다. \"셔럽 말포이!!\"");
	}
}
